// Regenerates src/data/activities.ts from GET /api/Configuration/game-data
// (Tasks.<SkillName> for every non-combat skill). Run with:
//   node scripts/generate-activities.mjs

const res = await fetch('https://query.idleclans.com/api/Configuration/game-data')
const raw = await res.text()
const cleaned = raw
  .replace(/ObjectId\("[a-f0-9]+"\)/g, 'null')
  .replace(/ISODate\("[^"]*"\)/g, 'null')
const data = JSON.parse(cleaned)

const SKILL_KEYS = Object.keys(data.Tasks).filter((k) => k !== 'Combat')

const activities = []
for (const skillKey of SKILL_KEYS) {
  const tasks = data.Tasks[skillKey].flatMap((group) => group.Items)
  for (const t of tasks) {
    if (t.Hidden || t.Disabled || t.BaseTime === 0) continue // BaseTime 0 = not a normal timed action (e.g. milestone unlock)
    activities.push({
      // TaskId is only unique *within* a skill (e.g. Mining TaskId 2 and
      // Smithing TaskId 2 both exist) — id must combine both to be unique.
      id: `${skillKey}_${t.TaskId}`,
      taskId: t.TaskId,
      name: t.Name,
      skillKey,
      levelRequired: t.LevelRequirement,
      baseActionsPerHour: Math.round(3_600_000 / t.BaseTime),
      xpPerAction: t.ExpReward,
      outputItems:
        t.ItemReward > 0 ? [{ itemId: t.ItemReward, quantity: t.ItemAmount || 1 }] : [],
      inputItems: (t.Costs ?? []).map((c) => ({ itemId: c.Item, quantity: c.Amount })),
    })
  }
}

activities.sort((a, b) => a.skillKey.localeCompare(b.skillKey) || a.taskId - b.taskId)

const header = `// Generated by scripts/generate-activities.mjs from GET /api/Configuration/game-data
// (Tasks.<skill>). Do not hand-edit — rerun the script to regenerate.

export interface ActivityItemAmount {
  itemId: number
  quantity: number
}

export interface ActivityDefinition {
  id: string // \`\${skillKey}_\${taskId}\` — taskId alone is only unique within a skill
  taskId: number
  name: string
  skillKey: string // raw API skill grouping, e.g. "Mining", "Smithing"
  levelRequired: number
  baseActionsPerHour: number // derived: 3,600,000 / BaseTime(ms)
  xpPerAction: number
  outputItems: ActivityItemAmount[]
  inputItems: ActivityItemAmount[]
}

export const ACTIVITIES: ActivityDefinition[] = `

const footer = `

export const ACTIVITIES_BY_ID: Map<string, ActivityDefinition> = new Map(ACTIVITIES.map((a) => [a.id, a]))
`

const fs = await import('node:fs/promises')
await fs.writeFile(
  new URL('../src/data/activities.ts', import.meta.url),
  header + JSON.stringify(activities, null, 2) + footer,
)

console.log(`Wrote ${activities.length} activities to src/data/activities.ts`)
