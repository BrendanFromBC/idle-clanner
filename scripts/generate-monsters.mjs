// Regenerates src/data/monsters.ts from GET /api/Configuration/game-data
// (Tasks.Combat). Run with: node scripts/generate-monsters.mjs
//
// Drop "rate" is derived as weight / sum(weights) within each monster's loot
// table — this assumes a single weighted roll per kill, which matches how
// Idle Clans' loot tables are commonly described, but hasn't been verified
// against in-game drop logs. Treat as an approximation, not a guarantee.

const res = await fetch('https://query.idleclans.com/api/Configuration/game-data')
const raw = await res.text()
const cleaned = raw
  .replace(/ObjectId\("[a-f0-9]+"\)/g, 'null')
  .replace(/ISODate\("[^"]*"\)/g, 'null')
const data = JSON.parse(cleaned)

const monsterTasks = data.Tasks.Combat.flatMap((group) => group.Items)

const monsters = monsterTasks
  .filter((m) => !m.Hidden && !m.Disabled)
  .map((m) => {
    const totalWeight = m.Loot.reduce((sum, drop) => sum + drop.Weight, 0)
    return {
      id: m.TaskId,
      name: m.Name,
      isBoss: m.IsBoss,
      isClanBoss: m.IsClanBoss,
      combatLevelRequirement: m.LevelRequirement,
      health: m.EnemyHealth,
      // Raw API code (0=None, 1=Stab, 2=Slash, 3=Pound, 4=Crush, 5=Archery,
      // 6=Magic, 7=None/varies — see src/utils/combatStyle.ts for how this
      // was verified). Kept raw here; decode at display time via
      // getWeaknessLabel(), not baked into the generated data.
      attackStyleWeakness: m.AttackStyleWeakness,
      defenceBonus: m.EnemyDefenceBonus,
      archeryDefenceBonus: m.EnemyArcheryDefenceBonus,
      magicDefenceBonus: m.EnemyMagicDefenceBonus,
      drops: m.Loot.map((drop) => ({
        itemId: drop.ItemId,
        quantityMin: drop.ItemAmountMin,
        quantityMax: drop.ItemAmountMax,
        weight: drop.Weight,
        dropRate: totalWeight > 0 ? drop.Weight / totalWeight : 0,
      })),
    }
  })

monsters.sort((a, b) => a.id - b.id)

const header = `// Generated by scripts/generate-monsters.mjs from GET /api/Configuration/game-data
// (Tasks.Combat). Do not hand-edit — rerun the script to regenerate.
//
// dropRate is derived (weight / total weight in the loot table), not a value
// the API states directly — see the script header for the assumption this
// relies on.

export interface MonsterDrop {
  itemId: number
  quantityMin: number
  quantityMax: number
  weight: number
  dropRate: number
}

export interface MonsterDefinition {
  id: number
  name: string
  isBoss: boolean
  isClanBoss: boolean
  combatLevelRequirement: number
  health: number
  attackStyleWeakness: number // raw code — decode via src/utils/combatStyle.ts getWeaknessLabel()
  defenceBonus: number
  archeryDefenceBonus: number
  magicDefenceBonus: number
  drops: MonsterDrop[]
}

export const MONSTERS: MonsterDefinition[] = `

const footer = `

export const MONSTERS_BY_ID: Map<number, MonsterDefinition> = new Map(MONSTERS.map((m) => [m.id, m]))
`

const fs = await import('node:fs/promises')
await fs.writeFile(
  new URL('../src/data/monsters.ts', import.meta.url),
  header + JSON.stringify(monsters, null, 2) + footer,
)

console.log(`Wrote ${monsters.length} monsters to src/data/monsters.ts`)
